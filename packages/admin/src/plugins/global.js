/**
 * 全局状态管理
 * 集中管理所有全局数据，避免分散到多个 store 文件
 */
export const useGlobal = defineStore("global", () => {
    // ==================== 全局数据 ====================
    const data = $ref({});

    // ==================== 全局方法 ====================
    const method = {};

    // ==================== 返回 ====================
    return {
        data,
        method
    };
});
